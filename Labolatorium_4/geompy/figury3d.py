import math

def objetosc_szescianu(a):
    return a**3

def pole_szescianu(a):
    return 6 * a * a

def objetosc_prostopadloscianu(a, b, c):
    return a * b * c

def pole_prostopadloscianu(a, b, c):
    return 2 *(a * b + a * c + b * c)

def objetosc_kuli(r):
    return (4/3) * math.pi * r**3

def pole_kuli(r):
    return 4 * math.pi * r**2

